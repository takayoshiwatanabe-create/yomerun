// Function to get messages for a specific locale
export const getMessages = (locale: SupportedLocale): Record<string, string> => {
  return translations[locale] || translations.ja; // Fallback to Japanese
};

// Hook to detect device locale and return the closest supported locale
export function useDeviceLocale(): SupportedLocale {
  const deviceLocales = Localization.getLocales();
  const preferredLocale = deviceLocales[0]?.languageCode;

  if (preferredLocale && locales.includes(preferredLocale as SupportedLocale)) {
    return preferredLocale as SupportedLocale;
  }

  // Fallback to default Japanese if device locale is not supported
  return 'ja';
}
